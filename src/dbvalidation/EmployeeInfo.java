package dbvalidation;

public class EmployeeInfo {

	
	 
		
		
		
	    private int empId;
	    private String FirstName;
	    private String LastName;
	    private int Age;
	    private String Location;
	    
	    
	   
	    
	    
	    
	    // getters
	    public int getEmpId(){
	    	
	    	return empId;}
	    
	    
	    public String getFirstName(){return FirstName;}
	    public String getLastName(){return LastName;}
	    public int getAge(){return Age;}
	    public String getLoc(){return Location;}
	    
	    //setters
	    public void  setEmpId(int val){empId=val;}
	    public void setFirstName(String val){FirstName=val;}
	    public void setLastName(String val){LastName=val;}
	    public void setAge(int val){Age=val;}
	    public void setLoc(String val){Location=val;}


		
	    
	}

	

